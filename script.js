let currentLang = "es";

// ===============================
// 🔄 APLICAR TRADUCCIONES
// ===============================
function applyTranslations(lang) {
  const dict = translations[lang];

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;

    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // Traducción de atributos (ej: aria-label)
  document.querySelectorAll("[data-i18n-attr]").forEach(el => {
    const attr = el.dataset.i18nAttr;
    const key = el.dataset.i18n;

    if (dict[key]) {
      el.setAttribute(attr, dict[key]);
    }
  });

  // Idioma del documento
  document.documentElement.lang = lang;

  // Botón idioma
  const btnLang = document.getElementById("toggle-lang");
  btnLang.textContent = lang.toUpperCase();

  // CV dinámico
  const cv = document.getElementById("cv-download");
  if (cv) {
    cv.href = lang === "en" ? "CV_EN.pdf" : "CV_ES.pdf";
  }

  localStorage.setItem("lang", lang);
}

// ===============================
// 🌗 TEMA OSCURO
// ===============================
function applyTheme(theme) {
  document.body.classList.toggle("dark", theme === "dark");
  localStorage.setItem("theme", theme);
}

document.getElementById("toggle-theme")?.addEventListener("click", () => {
  const isDark = document.body.classList.contains("dark");
  applyTheme(isDark ? "light" : "dark");
});

// ===============================
// 🌐 CAMBIO DE IDIOMA
// ===============================
document.getElementById("toggle-lang")?.addEventListener("click", () => {
  currentLang = currentLang === "es" ? "en" : "es";
  applyTranslations(currentLang);
});

// ===============================
// 📱 NAV RESPONSIVE
// ===============================
document.getElementById("icono-nav")?.addEventListener("click", () => {
  document.getElementById("nav")?.classList.toggle("open");
});

// ===============================
// 🚀 INIT
// ===============================
window.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("lang") || "es";
  const savedTheme = localStorage.getItem("theme") || "light";

  currentLang = savedLang;

  applyTranslations(savedLang);
  applyTheme(savedTheme);
});

